let currentLang = 'en';

// ------ Translation Helpers ------

function getTranslation(key) {
    const langData = TRANSLATIONS[currentLang];
    return langData ? langData[key] || TRANSLATIONS.en[key] || key : key;
}

function formatTranslation(key, params) {
    let text = getTranslation(key);
    if (params) {
        Object.keys(params).forEach(param => {
            text = text.replace(`{${param}}`, params[param]);
        });
    }
    return text;
}

// ------ Language Dropdown ------

function initLanguageDropdown() {
    const dropdown = document.getElementById('languageDropdown');
    const languageCodes = Object.keys(TRANSLATIONS).sort((a, b) => {
        return TRANSLATIONS[a].name.localeCompare(TRANSLATIONS[b].name);
    });

    dropdown.innerHTML = languageCodes.map(code => {
        const lang = TRANSLATIONS[code];
        const isActive = code === currentLang;
        return `<div class="atamas-language-option ${isActive ? 'active' : ''}" data-lang="${code}" style="font-family: ${lang.font};">${lang.name}</div>`;
    }).join('');

    document.querySelectorAll('.atamas-language-option').forEach(option => {
        option.addEventListener('click', () => {
            const langCode = option.dataset.lang;
            changeLanguage(langCode);
            closeLanguageDropdown();
        });
    });
}

function changeLanguage(langCode) {
    if (!TRANSLATIONS[langCode]) return;
    currentLang = langCode;
    localStorage.setItem('atamas_lang', langCode);

    updateLanguageDisplay();
    updateAllUI();
    initLanguageDropdown();
}

function updateLanguageDisplay() {
    const langBtnText = document.getElementById('currentLangText');
    const langBtn = document.getElementById('languageBtn');
    if (langBtnText && langBtn) {
        const lang = TRANSLATIONS[currentLang];
        langBtnText.textContent = lang.name;
        langBtn.style.fontFamily = lang.font;
    }
}

function openLanguageDropdown() {
    document.getElementById('languageSelect').classList.add('open');
}

function closeLanguageDropdown() {
    document.getElementById('languageSelect').classList.remove('open');
}

function loadSavedLanguage() {
    const savedLang = localStorage.getItem('atamas_lang');
    if (savedLang && TRANSLATIONS[savedLang]) {
        currentLang = savedLang;
    } else {
        const browserLang = navigator.language.substring(0, 2);
        if (TRANSLATIONS[browserLang]) {
            currentLang = browserLang;
        }
    }
}

